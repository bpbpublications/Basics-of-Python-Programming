#Example displaying the input() and format() functions
name = input("Enter Your Name")
age = input("Enter Your Age:")
output = "Name is {} and Age is {} years old.".format(name, age)
print(output)
#The above statement can also be written as:
result = f"Name is {name} and i am {age} years old."
#f symbolizes f-string format which offers a shorter method of formatting strings
print(result)



             

             
