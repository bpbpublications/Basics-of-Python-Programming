import numpy as np
# Creating a NumPy array
a1 = np.array([3,4,5,6,7])
print("NumPy array operations using pre-defined functions:")
print(a1)
# Finding sum,mean,max and min in Numpy
print("\nBasic Operations:")
print("Sum of array:", np.sum(a1))
print("Mean of array:", np.mean(a1))
print("Max of array:", np.max(a1))
print("Min of array:", np.min(a1))


